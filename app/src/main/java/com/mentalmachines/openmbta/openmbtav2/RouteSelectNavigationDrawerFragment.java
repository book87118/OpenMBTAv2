	/**
	 * Callbacks interface that all activities using this fragment must
	 * implement.
	 */
	public static interface NavigationDrawerCallbacks {
		/**
		 * Called when an item in the navigation drawer is selected.
		 */
		void onNavigationDrawerItemSelected(int position);
	}
	
	public void loadBusRoutes(){
		getRouteList();
	    RouteSelectAdapter adapter = new RouteSelectAdapter(getActivity(), R.layout.route_select_item, routes);
		mDrawerListView.setAdapter(adapter);
	}
	
	public void getRouteList(){
		String xmlResponse = null;
		Uri.Builder builder = new Uri.Builder();
		Serializer serializer = new Persister();
		RouteList routeList;
		builder.scheme("http").authority("webservices.nextbus.com").appendPath("service");
		builder.appendPath("publicXMLFeed").appendQueryParameter("command", "routeList").appendQueryParameter("a", "mbta");
		String URL = builder.toString();
		try {
			xmlResponse = new HttpRequestTask().execute(URL, "", "").get();
		} catch (InterruptedException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (ExecutionException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		try {
			routeList = serializer.read(RouteList.class, xmlResponse);
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		Log.i("OpenMBTAv2", xmlResponse);
	}
	
	public void getRouteConfig(Integer routeId){
		String xmlResponse = null;
		Uri.Builder builder = new Uri.Builder();
		Serializer serializer = new Persister();
		
		builder.scheme("http").authority("webservices.nextbus.com").appendPath("service");
		builder.appendPath("publicXMLFeed").appendQueryParameter("command", "routeConfig").appendQueryParameter("a", "mbta");
		builder.appendQueryParameter("r", routeId.toString());
		String URL = builder.toString();
		try {
			xmlResponse = new HttpRequestTask().execute(URL, "", "").get();
		} catch (InterruptedException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (ExecutionException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		try {
			routeConfig = serializer.read(RouteConfig.class, xmlResponse);
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		Log.i("OpenMBTAv2", xmlResponse);
	}
